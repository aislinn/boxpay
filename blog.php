  							<h2>What are the Differences Between Carrier Billing and NFC?</h2>
  							
  							<div class="byline">Posted by <a href="#">Amanda Keating</a> on Tue, Jul 03, 2012</div>
  							
  							<p>A special report published recently by Business Insider Intelligence reviews several mobile payment options, highlighting the pros and cons of each and analyzing their competing bids to gain dominance in the growing global mobile payment sector. Carrier billing and Near-Field Communication (NFC) solutions were two of the options reviewed.</p>
  							
  							<p>NFC is a wireless communication technology that enables a mobile device to establish contact with another NFC-enabled device when the two are brought in close proximity. Users generally have to touch the devices together or bring them to within a few inches of each other to establish transmission capabilities.</p>
  							
  							<p>NFC devices have gained some traction in the mobile payment industry via initiatives like the Google Wallet, which allows users to store credit card, coupon or customer loyalty program information in a virtual wallet app and make in-store payments or redeem discount coupons by tapping their mobile device to an NFC-enabled reader at the point of sale.</p>
  							
  							<p>Carrier billing mobile payment solutions allow consumers to charge transactions to their mobile phone bill or prepaid balance. Users arenâ€™t required to register for an account or provide credit card numbers; they simply confirm a purchase via text message.</p>
  							
  							<p>Carrier billing solutions are an increasingly popular way to make micropayments to gain access to digital content, buy items online, purchase games or make donations. The solution works anywhere mobile phone service is available, and it does not require users to have a bank or credit card account.</p>
  							
  							<p>Since NFC mobile payment solutions require proximity to the point of sale, they are generally not competing head-to-head with carrier billing solutions for market share in the fast-growing ecommerce space. NFC-based solutions may eventually replace plastic credit cards, paper coupons and customer loyalty cards, but for making payments on the go without the hassles and security worries associated with transmitting credit card information, carrier billing solutions remain the better option.</p>
  							
  							
